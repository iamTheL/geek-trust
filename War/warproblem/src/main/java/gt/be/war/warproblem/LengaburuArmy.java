public class LengaburuArmy {

	private Double sling;
	private Double tank;
	private Double elephant;
	private Double horse;
	private String result;
	
	
	
	/**
	 * @return the sling
	 */
	public Double getSling() {
		return sling;
	}
	/**
	 * @param sling the sling to set
	 */
	public void setSling(Double sling) {
		this.sling = sling;
	}
	/**
	 * @return the tank
	 */
	public Double getTank() {
		return tank;
	}
	/**
	 * @param tank the tank to set
	 */
	public void setTank(Double tank) {
		this.tank = tank;
	}
	/**
	 * @return the elephant
	 */
	public Double getElephant() {
		return elephant;
	}
	/**
	 * @param elephant the elephant to set
	 */
	public void setElephant(Double elephant) {
		this.elephant = elephant;
	}
	/**
	 * @return the horse
	 */
	public Double getHorse() {
		return horse;
	}
	/**
	 * @param horse the horse to set
	 */
	public void setHorse(Double horse) {
		this.horse = horse;
	}
	/**
	 * @return the result
	 */
	public String getResult() {
		return result;
	}
	/**
	 * @param result the result to set
	 */
	public void setResult(String result) {
		this.result = result;
	}
	
	
	
	

}
